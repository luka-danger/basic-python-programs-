# While Loop
n = 1
while n <= 5: 
   print(n)
   n += 1

# For Loops 
for i in range(5):
   print("hi" * i)

# (start, stop, decrement) 
for i in range(5, 0, -1):
    print(i)

# division 
print(5 / 2)
# division rounded down
print(5 // 2)
# division with negative numbers 
print(-3 / 2)

# Modulo 
print(10 % 3)

# Negative Value Mod
import math
print(math.fmod(-10, 3))

# Math Helper 
print(math.floor(3/2))
print(math.ceil(3/2))
print(math.sqrt(25))
print(math.pow(2,3))

